    public void tampil(){
        try{
            
            Object[] baris = {"nama", "nis", "rayon", "ruangtidur", "harga", "bayar", "kembalian"};
            tabModel = new DefaultTableModel(null, baris);
            st = cn.createStatement();
            rs = st.executeQuery("select * from tb_peserta");
            while(rs.next()){
                Object[] row = {
                    rs.getString("nama"),
                    rs.getString("nis"),
                    rs.getString("rayon"),
                    rs.getString("ruang_tidur"),
                    rs.getString("harga"),
                    rs.getString("bayar"),
                    rs.getString("kembalian")
                };
                tabModel.addRow(row);
            }
            table_peserta.setModel(tabModel);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
