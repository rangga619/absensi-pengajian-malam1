    private void cariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cariKeyReleased
        // TODO add your handling code here:
        try{
            st = cn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = st.executeQuery("select * from tb_peserta where  nis like '%" + cari.getText() + "%' or nama like '%" + cari.getText() + "%' or rayon like'%" + cari.getText() + "%' or ruang_tidur like '%" + cari.getText() + "%' or harga like'%" + cari.getText() + "%' or harga like'%" + cari.getText() + "%' or bayar like'%" + cari.getText() + "%' or kembalian like'%" + cari.getText() + "&'");
            while(rs.next()){
                Object[] data = {
            rs.getString("nama"),
            rs.getString("nis"),
            rs.getString("rayon"),
            rs.getString("ruang_tidur"),
            rs.getString("harga"),
            rs.getString("bayar"),
            rs.getString("kembalian")
            };
            tabModel.addRow(data);
        }
    }catch (SQLException e){
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }//GEN-LAST:event_cariKeyReleased

